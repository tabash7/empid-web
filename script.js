// Apply all copy, metadata, and direction changes from the translations object.
function translatePage(lang) {
  const dictionary = translations[lang] || translations.ar;
  currentLang = lang;

  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  document.title = dictionary["meta.title"];
  if (metaDescription) {
    metaDescription.setAttribute("content", dictionary["meta.description"]);
  }

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });

  document.querySelectorAll("[data-i18n-aria]").forEach((element) => {
    const key = element.getAttribute("data-i18n-aria");
    if (dictionary[key]) {
      element.setAttribute("aria-label", dictionary[key]);
    }
  });

  document.querySelectorAll(".lang-content").forEach((element) => {
    const shouldShow = element.classList.contains(`lang-${lang}`);
    element.hidden = !shouldShow;
  });

  langButtons.forEach((button) => {
    const isActive = button.dataset.lang === lang;
    button.classList.toggle("active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  if (formSubject) {
    formSubject.value = dictionary["form.subject"];
  }
  localStorage.setItem("empid-language", lang);
}

function closeMobileMenu() {
  document.body.classList.remove("nav-open");
  if (menuToggle) {
    menuToggle.setAttribute("aria-expanded", "false");
  }
}

langButtons.forEach((button) => {
  button.addEventListener("click", () => {
    translatePage(button.dataset.lang);
    closeMobileMenu();
  });
});

if (menuToggle) {
  menuToggle.addEventListener("click", () => {
    const isOpen = document.body.classList.toggle("nav-open");
    menuToggle.setAttribute("aria-expanded", String(isOpen));
  });
}

navLinks.forEach((link) => {
  link.addEventListener("click", (event) => {
    const targetId = link.getAttribute("href");
    if (!targetId || !targetId.startsWith("#")) return;

    const target = document.querySelector(targetId);
    if (!target) return;

    event.preventDefault();
    target.scrollIntoView({ behavior: "smooth", block: "start" });
    closeMobileMenu();
  });
});

